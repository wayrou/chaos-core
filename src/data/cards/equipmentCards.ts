import { EquipmentCard } from "../../core/equipment";

function sanitizeEquipmentCard(card: EquipmentCard): EquipmentCard {
    const description = card.description.trim();
    const lowerName = card.name.toLowerCase();
    const lowerDescription = description.toLowerCase();
    const isPlaceholder =
        lowerDescription.startsWith("use ") ||
        lowerDescription === "ranged attack." ||
        lowerDescription === "defensive action or stance.";

    if (!isPlaceholder) {
        return card;
    }

    const isSelfCard =
        (card.range || "").toLowerCase().includes("self") ||
        ["guard", "stance", "brace", "ward", "shield", "veil", "focus", "resolve", "dodge", "form", "barrier"].some(hint =>
            lowerName.includes(hint)
        );

    let nextDescription = description;
    if (isSelfCard) {
        nextDescription = "Gain +2 DEF until next turn.";
    } else if (typeof card.damage === "number" && card.damage > 0) {
        nextDescription = `Deal ${card.damage} damage.`;
    } else {
        nextDescription = "Apply a tactical effect to the target.";
    }

    return {
        ...card,
        description: nextDescription,
    };
}

